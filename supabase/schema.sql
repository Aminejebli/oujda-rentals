create table if not exists categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  slug text not null unique,
  created_at timestamptz not null default now()
);

create table if not exists agencies (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null unique,
  city text not null default 'Oujda',
  area text not null,
  phone text,
  whatsapp text not null,
  rating numeric(2, 1) not null default 4.5 check (rating >= 0 and rating <= 5),
  description text not null,
  created_at timestamptz not null default now()
);

create table if not exists cars (
  id bigint generated by default as identity primary key,
  agency_id bigint not null references agencies(id) on delete cascade,
  category_id bigint not null references categories(id) on delete restrict,
  name text not null,
  slug text not null unique,
  image_url text not null,
  price_per_day integer not null check (price_per_day > 0),
  transmission text not null check (transmission in ('Manual', 'Automatic')),
  seats integer not null check (seats > 0),
  fuel text not null,
  available boolean not null default true,
  created_at timestamptz not null default now()
);

create index if not exists cars_agency_id_idx on cars(agency_id);
create index if not exists cars_category_id_idx on cars(category_id);
create index if not exists cars_price_per_day_idx on cars(price_per_day);

alter table categories enable row level security;
alter table agencies enable row level security;
alter table cars enable row level security;

drop policy if exists "Public can read categories" on categories;
drop policy if exists "Public can read agencies" on agencies;
drop policy if exists "Public can read cars" on cars;

create policy "Public can read categories"
on categories for select
using (true);

create policy "Public can read agencies"
on agencies for select
using (true);

create policy "Public can read cars"
on cars for select
using (true);

grant select on categories to anon, authenticated;
grant select on agencies to anon, authenticated;
grant select on cars to anon, authenticated;

